class DateofBirth{
	int day;
	int month;
	int year;
	DateofBirth(int d,int m,int y){
		day=d;
		month=m;
		year=y;
	}

	void setday(int d){
		this.day=day;
	}
		int getday(){
		return day;
	}


	void setmonth(int m){
		this.month=month;
	}
	int getmonth(){
		return month;
	}

	void setyear(int year){
		this.year=year;
	}

	int getyear(){
		return year;
	}
}